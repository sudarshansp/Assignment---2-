#include <stdio.h>
int main()
{
  int a = sizeof(a);
  printf("size of int a is: %d byte",a);
  printf("\n");
  float b = sizeof(b);
  printf("size of float b is: %f byte",b);
  printf("\n");
  char c = sizeof(c);
  printf("size of char c is: %d byte",c);
  printf("\n");
  double d = sizeof(d);
  printf("size of double d is: %f byte",d);
}
